        //Write your custom code...
        
        [AllowAnonymous]
        public virtual async Task<double> GetAverageEvaluationForServiceAsync(Guid serviceId)
        {
            // Get all evaluations for the specified service
            var evaluations = await _serviceEvaluationRepository.GetListAsync(e => e.ImaarServiceId == serviceId);
            
            // If there are no evaluations, return 0
            if (!evaluations.Any())
            {
                return 0;
            }
            
            // Calculate the average rating
            double averageRating = evaluations.Average(e => e.Rate);
            
            return averageRating;
        }
    }

    {
        //<suite-custom-code-autogenerated>
        public ServiceEvaluationsAppService(IServiceEvaluationRepository serviceEvaluationRepository, ServiceEvaluationManager serviceEvaluationManager, IDistributedCache<ServiceEvaluationDownloadTokenCacheItem, string> downloadTokenCache, IRepository<Imaar.UserProfiles.UserProfile, Guid> userProfileRepository, IRepository<Imaar.ImaarServices.ImaarService, Guid> imaarServiceRepository)
